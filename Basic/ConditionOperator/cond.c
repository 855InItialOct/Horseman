#include <stdio.h>

int main()
{
    int left = 4,right = 3;
    printf("left == right:%d\n",left == right);
    printf("left != right:%d\n",left != right);
    printf("left > right:%d\n",left > right);
    printf("left < right:%d\n",left < right);
    printf("left >= right:%d\n",left >= right);
    printf("left <= right:%d\n",left <= right);

    return 0;
}